function colorBG(color) {
    switch (color) {
        case 'red':
            this.style.backgroundColor = "#FC6C6D";
            return "#FC6C6D"
        case 'green':
            this.style.backgroundColor = "#65a57f";
            return "#65a57f"
        case 'blue':
            this.style.backgroundColor = "#90c7ff";
            return "#90c7ff"
        case 'brown':
            this.style.backgroundColor = "#ad8f76";
            return "#ad8f76"
        case 'black':
            this.style.backgroundColor = "#2e2e2e";
            return "#2e2e2e"
        case 'pink':
            this.style.backgroundColor = "#f5bbc3";
            return "#f5bbc3"
        case 'gray':
            this.style.backgroundColor = "#bbbbbb";
            return "#bbbbbb"
        case 'purple':
            this.style.backgroundColor = "#ad90ac";
            return "#ad90ac"
        case 'white':
            this.style.backgroundColor = "#f7f7f7";
            return "#f7f7f7"
        case 'yellow':
            this.style.backgroundColor = "#ffd49e";
            return "#ffd49e"
    }
}